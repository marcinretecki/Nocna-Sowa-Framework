<p>Mamy dobrą wiadomość. Jeśli do tej pory solidnie robisz wszystkie wyzwania, to tworzenie wszystkich form rzeczownika powinno być proste.</p>

<p>Podsumujemy w tym miejscu wszystko, co wiesz na temat rzeczownika, tak żeby się to dobrze utrwaliło.</p>

<p>Nie wystarczy, że znasz ogólne zasady odmiany. Każda z form danego słowa powinna być dla Ciebie oczywista – w kolejności i na wyrywki, bo tylko wtedy będziesz swobodnie używać ich w mowie.</p>

<p>Jeśli znasz słowo <i class="mark">jenta</i> i nie wiesz czy to forma określona, czy nie, to z pewnością będziesz źle go używać w zdaniach. Bo to ma znaczenie, czy powiesz <i class="mark">ei jente</i> czy <i class="mark">jenta</i>, ale nigdy <i class="mark">ei jenta</i>.</p>

<p>Jeśli nie wiesz teraz o czym mówię to wróć do przewodnika: Czy rozróżniasz formę określoną od nieokreślonej? </p>

<p>Zakładam jednak, że to pamiętasz, więc przejdźmy do powtórki.</p>

<p>Każdy rzeczownik posiada rodzaj: męski, żeński lub nijaki. Odpowiadają im kolejno rodzajniki: <i class="mark">en</i>, <i class="mark">ei</i>, <i class="mark">et</i>.</i></p>

<p>Część Norwegów nie używa formy żeńskiej i wtedy wszystkie słowa <i class="mark">ei</i> używają w formie męskiej <i class="mark">en</i>.</p>

<p>Zamiast <i class="mark">ei jente</i> mówią <i class="mark">en jente</i>. Zamiast <i class="mark">jenta</i>, powiedzą <i class="mark">jenten</i>.</p>

<p>My uczymy wszystkich 3 form, bo jest to wbrew pozorom łatwiejsza droga. Dzięki temu będzie Ci później łatwiej odnaleźć się we wszystkich dialektach, zarówno w piśmie i w mowie. </p>

<p>Jeśli chcesz zrobić liczbę mnogą danego słowa, to zabierasz rodzajnik i dodajesz końcówkę <i class="mark">-er</i>.</p>

<dl class="przewodnik-eg">
  <dt>ei jente – jenter</dt>
</dl>

<p>Jest to forma nieokreślona. Powiedzielibyśmy po polsku, że jest to jakaś dziewczyna i jakieś dziewczyny.</p>

<p>Żeby zrobić liczbę mnogą określoną, do formy podstawowej dodajemy zawsze końcówkę <i class="mark">-ene</i>.</p>

<dl class="przewodnik-eg">
  <dt>ei jente – jentene</dt>
</dl>

<p>Zauważ, że nie podwajamy tu końcówki <i class="mark">-e</i>.</p>

<p>Końcówki i rodzajniki są bardzo ważne w codziennej komunikacji, bo mogą zmienić sens zdania.</p>

<p>Kiedy powiesz: <i class="mark">Jeg har et barn</i>. Oznacza to, że masz jedno dziecko.</p>

<p>Mówąc: <i class="mark">Jeg har barn</i>. Mówisz, że masz dzieci.</p>

<p>No właśnie, Czy pamiętasz co się dzieje ze słowami jednosylabowymi <i class="mark">-et</i>? Np. <i class="mark">et fjell</i>, <i class="mark">et spor</i>, <i class="mark">et dyr</i>.</p>

<p>W liczbie mnogiej nie dodajemy końcówki <i class="mark">-er</i></p>

<dl class="przewodnik-eg">
  <dt>fjell – fjellene</dt>
  <dd>góry</dd>
  <dt>spor – sporene</dt>
  <dd>tory, ślady</dd>
  <dt>dyr – dyrene</dt>
  <dd>zwierzęta</dd>
</dl>

<p>A co z nazwami zawodów? Czy odmieniają się regularnie? Np. <i class="mark">en maler</i>, <i class="mark">en kunstner</i>, <i class="mark">en forsker</i>.</p>

<p>Rzeczowniki zakończone na <i class="mark">-er</i> lub <i class="mark">-el</i> mają nieregularną odmianę.</p>

<dl class="przewodnik-eg">
  <dt>malere – malerne</dt>
  <dd>malarze</dd>
  <dt>kunstnere – kunstnerne</dt>
  <dd>artyści</dd>
  <dt>forskere – forskerne</dt>
  <dd>naukowcy</dd>
</dl>

<p>Żeby nabrać wprawy i odpowiedniego tempa, musisz takich przykładów zrobić setki. (Nie zamierzamy oszukiwać Cię, że może być tu jakaś droga na skróty.)</p>

<p>Dlatego, przyjmij sowie wyzwanie i sprawdź, czy radzisz sobie z odmianą rzeczownika na wyrywki.</p>