<p>Pamiętasz jak mówiliśmy, że <i class="mark">ikke</i> stawiamy za pierwszym czasownikiem?</p>

<p>To dobrze, bo takich słów jak <i class="mark">ikke</i> jest więcej. Nazywamy je okolicznikami zdaniowymi. Jeśli za jakiś czas zapomnisz tę nazwę to nic się nie stanie, ale mamy nadzieję, że zapamiętasz które słowa stawiać w tym miejscu zdania.</p>

<p>Oto kilka przykładów na start:</p>

<dl class="dl-eg przewodnik-eg">
  <dt>gjerne
  <dd>chętnie, z radością</dd>

  <dt>dessverre</dt>
  <dd>niestety</dd>

  <dt>sannsynligvis</dt>
  <dd>prawdopodobnie</dd>

  <dt>heldigvis</dt>
  <dd>na szczęście</dd>

  <dt>alltid</dt>
  <dd>zawsze</dd>

  <dt>aldri</dt>
  <dd>nigdy</dd>

  <dt>ofte</dt>
  <dd>często</dd>

  <dt>sjelden</dt>
  <dd>rzadko</dd>

  <dt>vanligvis</dt>
  <dd>zazwyczaj</dd>

  <dt>jo</dt>
  <dd>przecież</dd>

  <dt>sikkert</dt>
  <dd>pewnie</dd>

  <dt>faktisk</dt>
  <dd>faktycznie</dd>

</dl>

<p>Ta lista mogłaby być znacznie dłuższa, ale nie ma sensu tworzyć jednej ze wszystkimi okolicznikami zdaniowymi. Nie musisz znać wszystkich, a już na pewno nie na samym początku nauki. </p>

<p>Ważniejsze jest, żeby nauczyć się je zauważać samemu i stopniowo rozbudowywać słownictwo o takie słowa, których potrzebujesz.</p>

<p>Zobacz jak używać ich w zdaniach. To proste.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Jeg vil gjerne snakke med Andreas.</dt>
  <dd>Chętnie porozmawiam z Andreasem.</dd>

  <dt>Han er sannsynligvis på ferie nå.</dt>
  <dd>On jest prawdopodobnie na urlopie teraz.</dd>

  <dt>Han er vanligvis uten mobilen.</dt>
  <dd>On jest zazwyczaj bez telefonu (komórkowego).</dd>

  <dt>Jeg må dessverre vente.</dt>
  <dd>Muszę niestety poczekać.</dd>

  <dt>Jeg er sjelden så stresset.</dt>
  <dd>Żadko jestem tak zestresowana.</dd>

  <dt>Jeg er alltid alene.</dt>
  <dd>Zawsze jestem sama.</dd>

  <dt>Jeg skal aldri reise med fly.</dt>
  <dd>Nie zamierzam nigdy podróżować samolotem.</dd>

  <dt>Vi blir heldigvis på stedet om en stund.</dt>
  <dd>Będziemy na szczęście na miejscu za chwilę.</dd>

  <dt>Det gjør ikke noe.</dt>
  <dd>Nie szkodzi.</dd>

  <dt>Jeg kan sikkert ta toget.</dt>
  <dd>Mogę z pewnością pojechać pociągiem.</dd>

  <dt>Det er faktisk lett.</dt>
  <dd>To jest rzeczywiście łatwe.</dd>

  <dt>Det skjer jo ikke ofte.</dt>
  <dd>To przecież nie zdarza się często.</dd>
</dl>

<p>Zapamiętaj, że okoliczniki zdaniowe stawiamy za pierwszym czasownikiem w szyku prostym.</p>

