<p>W języku polskim kolejność podawania informacji nie ma dużego znaczenia, bo dzięki odmianie przez osoby i deklinacji, wiemy, co czego dotyczy.</p>

<p>Po norwesku nie odmieniamy rzeczowników przez przypadki, ani czasowników przez osoby. Dzięki temu nie musimy uczyć się kilkunastu form każdego słowa, jak muszą obcokrajowcy w Polsce.</p>

<p>Za to jest kilka zasad budowania zdań, które są ważne, żeby Norwegowie Cię zrozumieli.</p>

<p>Pokażemy Ci to w możliwie prostej formie, żeby od razu dało się zastosować wszystko w praktyce.</p>


<p class="con">podmiot + orzeczenie + *ikke* + co? jak? gdzie? kiedy? dlaczego?</p>


<p>Kiedy zaczynasz norweskie zdanie najłatwiej będzie zacząć je od podmiotu.</p>

<p>Na drugiej pozycji będzie zawsze orzeczenie.</p>

<p>Tutaj mógłby być już koniec poprawnego zdania, ale możesz powiedzieć więcej.</p>

<p>Jeśli chcesz stworzyć przeczenie, to <i class="mark">ikke</i> będzie zaraz za pierwszym czasownikiem. Może tu stać też inny okolicznik zdaniowy, jak omówiliśmy w poprzednim przewodniku.</p>

<p>Następne informacje będą w takiej kolejności, by odpowiadały na pytania: <i class="mark">co? jak? gdzie? kiedy? dlaczego?</i></p>



<p>Zapamiętaj ich kolejność, bo to znacznie łatwiejsze niż gramatyczne nazwy: orzecznik czasownikowy, dopełnienie bliższe, dopełnienie dalsze, okolicznik sposobu, miejsca, czasu, przyczyny.</p>

<p>Wystarczy: <i class="mark">co? jak? gdzie? kiedy? dlaczego?</i></p>

<p>Przeanalizujmy razem kilka przykładów.</p>

<p>Zacznijmy od imienia:</p>

<dl class="przewodnik-eg">
  <dt>Gorm</dt>
</dl>

<p><i class="mark">Gorm</i> to męskie imię. Gdzie je wstawimy?</p>

<p>Oczywiście na miejscu podmiotu.</p>

<dl class="przewodnik-eg">
  <dt>leser</dt>
  <dd>czyta</dd>
</dl>

<p>Widzisz końcówkę <i class="mark">-r</i>, więc wiesz, że to czasownik odmieniony w czasie teraźniejszym.</p>

<dl class="przewodnik-eg">
  <dt>ofte</dt>
  <dd>często</dd>
</dl>

<p>To okolicznik zdaniowy, prawda?</p>

<dl class="przewodnik-eg">
  <dt>folkeeventyr</dt>
  <dd>baśnie</dd>
</dl>

<p>Gorm czyta często <i class="mark">co?</i> baśnie. Pasuje?</p>

<dl class="przewodnik-eg">
  <dt>i senga</dt>
  <dd>w łóżku</dd>
</dl>

<p>Na jakie pytanie odpowiada <q>w łóżku</q>? Czy będzie to <i class="mark">gdzie?</i> Jasne.</p>

<p>To zdanie jest już ok, ale możemy dołączyć jeszcze:</p>

<dl class="przewodnik-eg">
  <dt>om natta</dt>
  <dd>nocą, nocami</dd>
</dl>

<p>Na jakie pytanie odpowiada <q>nocą</q>? Oczywiście jest to <i class="mark">kiedy?</i></p>

<p>A czy możemy dołożyć jeszcze <i class="mark">dlaczego?</i></p>

<p><q>bo wtedy ma czas</q></p>

<dl class="przewodnik-eg">
  <dt>fordi han har tid da.</dt>
  <dd>bo wtedy ma czas</dd>
</dl>

<dl class="przewodnik-eg">
  <dt>Gorm leser folkeeventyr i senga om natta fordi han har tid da.</dt>
  <dd>Gorm czyta baśnie w łóżku wieczorami, bo wtedy ma czas.</dd>
</dl>

<p>Dobrze. Zróbmy jeszcze jedno zdanie.</p>

<dl class="przewodnik-eg">
  <dt>ligger</dt>
  <dd>leży, leżą itd</dd>
</dl>

<p>Czy jest to czasownik? W czasie teraźniejszym? Czyli jest to orzeczenie.</p>

<dl class="przewodnik-eg">
  <dt>skogen</dt>
  <dd>las</dd>
</dl>

<p>Czy las może być podmiotem? <q>Las leży</q>, pasuje.</p>

<dl class="przewodnik-eg">
  <dt>ved elva</dt>
  <dd>nad rzeką</dd>
</dl>

<p>Na jakie pytanie to odpowiada? Oczywiście <i class="mark">gdzie?</i></p>

<dl class="przewodnik-eg">
  <dt>i nærheten</dt>
  <dd>w pobliżu</dd>
</dl>

<p>Hmm, znów mamy frazę, która odpowiada na pytanie <i class="mark">gdzie?</i> Czasem możemy stworzyć zdanie, w którym okoliczniki będą bardzo rozbudowane. Bo moglibyśmy dołożyć jeszcze:</p>

<dl class="przewodnik-eg">
  <dt>av huset vårt</dt>
  <dd>naszego domu</dd>
</dl>

<p>I to wszystko jest w miejscu <i class="mark">gdzie?</i>.</p>

<dl class="przewodnik-eg">
  <dt>Skogen ligger ved elva i nærheten av huset vårt.</dd>
</dl>

<p>Jeszcze jedno zdanie.</p>

<dl class="przewodnik-eg">
  <dt>kan</dt>
  <dd>może, umie, potrafi</dd>
</dl>

<p>Czas teraźniejszy? Czyli orzeczenie.</p>

<dl class="przewodnik-eg">
  <dt>man</dt>
  <dd>ktoś, człowiek</dd>
</dl>

<p>Pamiętasz <i class="mark">man</i>? Używaliśmy tego słowa, gdy zdanie było ogólną zasadą albo powiedzeniem, żeby nie mówić bezpośrednio do kogoś, jak przy zaimkach <i class="mark">du</i> lub <i class="mark">de</i>. <i class="mark">Man</i> będzie więc podmiotem.</p>

<dl class="przewodnik-eg">
  <dt>snakke</dt>
  <dd>rozmawiać</dd>
</dl>

<p>Jest to bezokolicznik, prawda? Napewno dobrze pamiętasz, że nie ma przed nim <i class="mark">å</i>, bo w zdaniu mamy czasownik modalny <i class="mark">kan</i>. Ale gdzie dopasować drugi czasownik do naszego schematu? W bardziej zaawansowanych wyzwaniach omówimy to dokładniej, ale teraz możesz pomyśleć tak.</p>

<p><i class="mark">Co można?</i> – można rozmawiać.</p>

<dl class="przewodnik-eg">
  <dt>stille</dt>
  <dd>cicho, spokojnie</dd>
</dl>

<p>Na jakie pytanie odpowie <q>cicho</q>? </p>

<p><i class="mark">Jak można rozmawiać?</i> – cicho. Czyli <i class="mark">jak?</i></p>

<dl class="przewodnik-eg">
  <dt>her</dt>
  <dd>tutaj</dd>
</dl>

<p>Tutaj, to oczywiście <i class="mark">gdzie?</i>.</p>

<dl class="przewodnik-eg">
  <dt>bare</dt>
  <dd>tylko, jedynie</dd>
</dl>

<p>To jest jeden z okoliczników zdaniowych. Gdzie pasuje? Tam, gdzie <i class="mark">ikke</i>.</p>

<dl class="przewodnik-eg">
  <dt>Man kan bare snakke stille her.</dd>
</dl>

<p>Może teraz to Ci się wydawać strasznie sztywne i nudne. Dlaczego wszystko musi być w takiej, a nie innej kolejności? Czy nie możemy czegoś przestawić.</p>

<p>Powoli. Na przestawianie szyku też są zasady, ale nie chcielibyśmy zasypać Cię wszystkim na raz i oczekiwać, że jakoś się z tym uporasz. </p>

<p>Dużo lepszą metodą jest opanowanie najpierw podstawy, żeby później przejść do trudniejszych rzeczy.</p>

<p>Jak w matematyce, jeśli nie umiesz dodawać, to czy dasz sobie radę z mnożeniem? Najpierw trzeba opanować jedno, żeby drugie miało sens.</p>

<p>Tak samo jest z norweskim szykiem. Najpierw opanujemy szyk prosty, a później będziemy go przestawiać.</p>

<p>W wyzwaniu mamy dla Ciebie więcej rozbudowanych zdań. <i class="mark">Lykke til!</i></p>

