<p>Prawie każdy kto zaczyna naukę norweskiego ma taki pomysł, żeby pominąć naukę rodzajników. No bo co to właściwie jest ten rodzajnik: <i class="mark">en</i>, <i class="mark">ei</i>, <i class="mark">et</i>. Czy on coś znaczy? Na przykłąd w słowie <i class="mark">en vei</i>. Jeśli powiem samo <i class="mark">vei</i> to przecież każdy zrozumie, że to droga. Wydaje się, że wtedy szybciej nauczymy się słów bez rodzajnika. </p>

