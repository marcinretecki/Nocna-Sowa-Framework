<p>Rodzajniki świadczą o tym, czy słowo jest rodzaju męskiego, żeńskiego czy nijakiego.</p>

<p>Musisz jeszcze wiedzieć, że rodzaje w języku polskim nie zgadzają się z norweskim np. po norwesku słońce jest rodzaju żeńskiego: <i class="mark">ei sol</i>, natomiast po polsku nijakiego: to słońce, a nie ta słońce.</p>

<p>No tak, ale dzieci przecież nie uczą się z rodzajnikiem? To prawda. A skąd Ty wiesz, że po polsku jest: ten dom, a nie ta dom? Bo rodzice, nauczyciele i babcia poprawiali Cię na każdym kroku. Przez wiele lat słyszałeś poprawne formy.</p>

