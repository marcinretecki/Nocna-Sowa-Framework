## Szersze wyjaśnienie, co to jest i jakie da Tobie wyniki, benefity itp, jak jest inny

Las to indywidualny kurs języka norweskiego przez internet, w którym wcielasz się w bohatera wędrujacego przez norweskie lasy.

Na każdym etapie wędrówki czeka na Ciebie przewodnik, wyzwanie i ratownik.

Przewodnik pomoże Ci zrozumieć zagadnienie i przygotować się do wyzwania.

W wyzwaniu, które możesz podjąć wiele razy, przećwiczysz zagadnienie w mowie.

W razie czego zawsze jest ratownik, który pomoże Ci rozwiać wątpliwości i nakieruje na dobrą ścieżkę.

Między wyzwaniami trafisz na boczne ścieżki, niezwykłe znaleziska i wciągające historie.


