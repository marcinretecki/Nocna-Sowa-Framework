<p>Przećwiczyliśmy już tworzenie formy nieokreślonej i określonej w liczbie pojedynczej. Została nam jeszcze liczba mnoga.</p>

<p>Nikogo nie trzeba przekonywać, że liczba mnoga jest potrzebna. Często mówimy przecież o kilku, a nie tylko jednej rzeczy. </p>

<p>Problem pojawia się kiedy musimy nauczyć się dwóch form: określonej i nieokreślonej. Na to nasz polski sposób myślenia nie jest przygotowany.</p>

<p>Za to dla każdego Norwega jest to normalne, że przedmioty mogą być określone lub nie, więc chcielibyśmy, żeby i dla Ciebie było to oczywiste. </p>

<p>Jeśli ominiesz to zagadnienie, to nie będziesz w stanie opanować później kolejnych rzeczy, jak np. zgodności przymiotnika z rzeczownikiem. </p>

<p>Rozróżniając wszystkie formy rzeczownika, będzie Ci o wiele łatwiej domyślić się sensu zdania albo wyszukać w słowniku nowe słowa.</p>

<p>Zobaczysz, że to małe zagadnienie gramatyczne będzie Twoim przyjacielem, a nie wrogiem.</p>

<p>Najprościej mówiąc w liczbie mnogiej nieokreślonej dodajemy końcówkę <i class="mark">-er</i>.</p>

<p>Jeden <q>ptak</q> to <i class="mark">en fugl</i>. Gdy chcemy powiedzieć o kilku dowolnych <q>ptakach</q>, zabieramy rodzajnik i dodajmy <i class="mark">-er</i> – <i class="mark">fugler</i>.</p>

<dl class="przewodnik-eg">
  <dt>en fugl – fugler</dt>
  <dd>ptak – ptaki</dd>
</dl>

<p>W rodzaju <i class="mark">ei</i> będzie tak samo. Jakieś jedno <q>ziele</q> to <i class="mark">ei urt</i>. <q>Zioła</q>, nieokreślone to <i class="mark">urter</i>. </p>

<dl class="przewodnik-eg">
  <dt>ei urt – urter</dt>
</dl>

<p>Jeden <q>człowiek</q> to <i class="mark">et menneske</i>. Gdy mówimy o kilku <q>ludziach</q> to powiemy <i class="mark">mennesker</i>. Znikł rodzajnik i dodaliśmy <i class="mark">-er</i>. </p>

<dl class="przewodnik-eg">
  <dt>et menneske – mennesker</dt>
  <dd>człowiek – ludzie</dd>
</dl>

<p>Zauważ, że nie podwoiliśmy samogłoski <i class="mark">e</i>. </p>

<p>To była forma nieokreślona. W formie określonej, gdy mówimy o konkretnych ludziach, zwierzętach czy przedmiotach, dodamy końcówkę <i class="mark">-ene</i>.</p>

<p>Końcówki dodajemy zawsze do podstawowej, nieokreślonej formy. To ważne.</p>

<p>Wróćmy do słowa <q>ptak</q>. Jeden nieokreślony to <i class="mark">en fugl</i>. Jeden określony – <i class="mark">fuglen</i>. A wiele określonych to <i class="mark">fuglene</i>.</p>

<dl class="przewodnik-eg">
  <dt>en fugl</dt>
  <dt>fuglen – fuglene</dt>
  <dd>ptak – ptaki</dd>
</dl>

<p>Podobnie będzie z ziołami: </p>

<dl class="przewodnik-eg">
  <dt>ei urt</dt>
  <dt>urta – urtene</dt>
  <dd>ziele – zioła</dd>
</dl>

<p>I z ludźmi:</p>

<dl class="przewodnik-eg">
  <dt>et menneske</dt>
  <dt>mennesket – menneskene</dt>
  <dd>człowiek – ludzie</dd>
</dl>

<p>Z tymi końcówkami będziesz już zawsze funkcjonować. Spotkasz je w każdym tekście i pewnie w każdej wypowiedzi.</p>

<p>Razem z liczbą mnogą mamy już całą odmianę rzeczownika.</p>

<p><i class="mark">En</i>, <i class="mark">ei</i>, <i class="mark">et</i> to rodzajniki, których używamy w formie nieokreślonej pojedynczej.</p>

<p>W formie określonej pojedynczej znika rodzajnik i dodajemy końcówki zgodnie z rodzajem: <i class="mark">-en</i>, <i class="mark">-a</i>, <i class="mark">-et</i>.</p>

<p><i class="mark">-Er</i> dodajemy w liczbie mnogiej, mówiąc o jakichś przedmiotach – nieokreślonych.</p>

<p><i class="mark">-Ene</i> dodajemy w liczbie mnogiej mówiąc o konkretnych rzeczach – określonych.</p>

<dl class="przewodnik-eg">
  <dt>en kaktus – kaktusen – kaktuser – kaktusene</dt>
  <dd>kaktus</dd>
  <dt>et insekt – insektet – insekter – insektene</dt>
  <dd>owad</dd>
</dl>

<p>W tej kolejności, rzeczowniki są podane w słowniku Bokmålsordboka, w którym wspólnie wyszukiwaliśmy słowa.</p>

<p>{screen ze słownika}</p>

<p>Tak też będą podane w słowniku Lexin.</p>

<p>{screen ze słownika}</p>

<p>Jest jeszcze jedna reguła, którą musisz zapamiętać.</p>

<p>Gotowy?</p>

<p>Rzeczowniki jednosylabowe rodzaju nijakiego, czyli <i class="mark">-et</i>, nie otrzymują końcówki <i class="mark">-er</i> w liczbie mnogiej.</p>

<p>Brzmi skomplikowanie?</p>

<p>Jeszcze raz. Rzeczowniki jednosylabowe jak np. <i class="mark">et smil</i>, <i class="mark">et dyr</i>, <i class="mark">et fjell</i>, nie otrzymują końcówki <i class="mark">-er</i>.</p>

<dl class="przewodnik-eg">
  <dt>et smil – smilet – smil – smilene</dt>
  <dd>uśmiech</dd>
  <dt>et fjell – fjellet – fjell – fjellene</dt>
  <dd>góra</dd>
  <dt>et isfjell – isfjellet – isfjell – isfjellene</dt>
  <dd>góra lodowa</dd>
</dl>

<p>Jak widzisz <i class="mark">-ene</i> otrzymują wszystkie słowa.</p>

<dl class="przewodnik-eg">
  <dt>et dyr – dyret – dyr – dyrene</dt>
  <dd>zwierzę</dd>
  <dt>et rådyr – rådyret – rådyr – rådyrene</dt>
  <dd>sarna</dd>
</dl>

<p>Jak na początek nauki, to tych zasad trochę jest, prawda? Możesz zastanawiać się też czy takich słów jednosylabowych <i class="mark">et</i> jest dużo i czy można tę zasadę pominąć?</p>

<p>Niestety jest ich bardzo dużo i nawet nie da się ich nauczyć z jakiejś listy. Przypuszczam, że nikt nie próbował nawet takiej stworzyć. </p>

<p>Na dodatek często będziesz zaglądać do słownika, żeby sprawdzić, czy słowo odmienia się zgodnie z zasadami. To zdecydowanie najlepszy nawyk.</p>

<p>Nie pozostaje Ci nic innego, jak to zaakceptować, wziąć głęboki oddech i przejść przez kolejne wyzwanie.</p>

<p?To co dla Ciebie przygotowaliśmy, powinno Ci bardzo w tym pomóc.</p>