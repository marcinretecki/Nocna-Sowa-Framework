<p>Wiele osób uważa, że rzeczowniki policzalne i niepoliczalne to trudna rzecz. A to nie jest przecież nic do zakuwania, tylko kwestia załapania pewnej idei. </p>

<p>To powinna być dobra wiadomość, bo jeśli to teraz zrozumiesz, to nie będziesz mieć problemów z tym, kiedy użyć rodzajnika, a kiedy nie. Albo kiedy używać tylko liczby pojedynczej, bo mnoga nie ma sensu.</p>

<p>Zacznijmy od tego, co to są rzeczowniki policzalne. To takie przedmioty lub osoby, które można policzyć. Na przykład: </p>

<ul>
<li>sowy – <i class="mark">ei ugle</i>, <i class="mark">to ugler</i>, <i class="mark">tre ugler</i></li>
<li>albo scieżki – <i class="mark">en sti</i>, <i class="mark">to stier</i>, <i class="mark">tre stier</i> </li>
<li>lub drzewa – <i class="mark">et tre</i>, <i class="mark">to trær</i>, <i class="mark">tre trær</i></li>
</ul>

<p>W tych słowach używamy rodzajnika <i class="mark">en</i>, <i class="mark">ei</i> lub <i class="mark">et</i> gdy mówimy o jednej sztuce. W liczbie mnogiej dodajemy końcówki <i class="mark">-er</i> lub <i class="mark">-ene</i>.</p>

<p>Nie wszystkie rzeczy możesz policzyć na sztuki – nazywamy je niepoliczalnymi. Są to na przykład substancje (syrop), materiały (beton), emocje (bezsilność), pojęcia abstrakcyjne (ciepło, zimno, dobro).</p>

<p>Do określenia ich ilości nie używamy sztuk, tylko dodatkowych jednostek jak litry albo kilogramy. Emocji nawet w taki sposób nie da się określić.</p>

<p>Cała odmiana tych słów wygląda tak:</p>

<dl class="przewodnik-eg">
  <dt>sirup – sirupen</dt>
  <dd>syrop</dd>
  <dt>betong – betongen</dt>
  <dd>beton</dd>
  <dt>avmakt – avmakta</dt>
  <dd>bezsilność</dd>
  <dt>varme – varmen</dt>
  <dd>ciepło</dd>
  <dt>kulde – kulda</dt>
  <dd>zimno</dd>
  <dt>godhet – godheta</dt>
  <dd>dobro</dd>
</dl>

<p>Widzisz, że w słowach niepoliczalnych nie ma rodzajnika, ani liczby mnogiej? To dobrze.</p>

<p>Teraz jeszcze ważna rzecz. W słowniku <i class="mark">Bokmålsordboka</i> znajdziesz często wszystkie 4 formy gramatyczne, także z liczbą mnogą. To nie znaczy, że będziesz ich w taki sposób używać. </p>

<p>Idea, którą Ci teraz przekazujemy dotyczy tego, jak używamy tych rzeczowników w praktyce i w życiu codziennym.</p>

<p>Pomocny może być w tym słownik Lexin, w którym pominięto formy, które wydawały się nieuzasadnione w codziennym użyciu.</p>

<p>W większości sytuacji musisz zdać się jednak na własną logikę.</p>

<p>Czy kawa, taka zmielona będzie policzalna? Dodasz rodzajnik? Stworzysz liczbę mnogą? </p>

<p>Oczywiście nie. Ale jeśli masz na myśli masz dwa kubki kawy, to śmiało możesz powiedzieć: <i class="mark">to kaffer</i>. Podobnie <i class="mark">to vann</i> albo <i class="mark">2 sjokolader</i>.</p>

<p>Jak w takim razie notować sobie rzeczowniki niepoliczalne, żeby się nie myliły?</p>

