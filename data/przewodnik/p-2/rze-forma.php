<p>Uczysz się już słów razem z rodzajnikiem. Na przykład <i class="mark">en skog</i> – las. Gdy używasz rzeczownika razem z rodzajnikiem, nazywamy to formą nieokreśloną.</p>

<p>W tym przewodniku omówimy formę określoną rzeczownika. Określony <q>las</q> to <i class="mark">skogen</i>.</p>

<dl class="przewodnik-eg">
  <dt>en skog – skogen</dt>
  <dd>las</dd>
</dl>

<p>Obie te formy, <i class="mark">en skog</i> i <i class="mark">skogen</i> oznaczają <q>las</q>, więc niektórzy myślą, że nie ma znaczenia, której formy się użyje.</p>

<p>W dodatku w języku polskim nie mamy rozróżnienia na formę określoną i nieokreśloną. Łatwo więc pomyśleć, że jest to nieważne zagadnienie.</p>

<p>Jednak my chcielibyśmy pokazać Ci norweski, takim jaki jest używany na codzień, przez Norwegów. A oni używają obu form w odpowiednich sytuacjach.</p>

<p>Jeśli to ominiesz, w pewnym momencie zatrzymasz się z nauką i nie pójdziesz dalej. Tam gdzie to możliwe, powiemy Ci, gdzie można zrobić skróty, ale tym razem nie mamy innej drogi.</p>

<p><i class="mark">En skog</i> możemy przetłumaczyć na <q>jakiś las</q>, dowolny, nieznany, pierwszy raz wspomniany, z którym dopiero się zaznajamiasz. W tej formie przed rzeczownikiem stoi rodzajnik.</p>

<p><i class="mark">Skogen</i> to <q>ten las</q> – też jeden, ale konkretny, znany Tobie i Twojemu rozmówcy. Rodzajnik zniknął, ale dodaliśmy końcówkę <i class="mark">-en</i>, która jest dla rodzaju męskiego.</p>

<p>To znaczy, że wszystkich nowych rzeczowników uczysz się w formie nieokreślonej. I to od tej podstawowej formy, możesz stworzyć formę określoną, a później też liczbę mnogą.</p>

<p>Las był rodzaju <i class="mark">en</i>, a co, jeśli rzeczownik jest rodzaju <i class="mark">ei</i>?</p>

<p><i class="mark">Ei sol</i> znaczy <q>jakieś słońce</q>. Jest rodzajnik, więc mamy formę nieokreśloną.</p>

<p>Ale słońce na niebie jest tylko jedno, więc z reguły mówimy o nim w formie określonej.</p>

<p>Dla rodzaju <i class="mark">ei</i> dodamy końcówkę <i class="mark">-a</i>.</p>

<dl class="przewodnik-eg">
  <dt>ei sol – sola</dt>
  <dd>słońce</dd>
</dl>

<p>W formie określonej nie ma już rodzajnika. Gdy chcesz powiedzieć słońce świeci, powiesz:</p>

<p><i class="mark">Sola skinner.</i> – konkretne słońce, to które wszyscy znają, świeci.</p>

<p>Został nam jeszcze rodzaj <i class="mark">et</i>.</p>

<p>Trawa to <i class="mark">et gress</i>. Zabieramy więc rodzajnik i do podstawowej formy dodajemy końcówkę <i class="mark">-et</i>. <i class="mark">Gresset</i> to ta konkretna trawa.</p>

<p>Zauważ, że nie czytamy tu ostatniego <i class="mark">-t</i>. Tak będzie we wszystkich określonych słowach rodzaju <i class="mark">et</i>.</p>

<p>Krótkie podsumowanie:</p>

<dl class="przewodnik-eg">
  <dt>en skog – skogen</dt>
  <dd>las</dd>
  <dt>ei sol – sola</dt>
  <dd>słońce</dd>
  <dt>et gress – gresset</dt>
  <dd>trawa</dd>
  <dd>nie czytamy <i class="mark">-t</i></dd>
</dl>

<p>Czy to znaczy, że każdego słowa musisz uczyć się w obu formach? Nic takiego! Norweskie rzeczowniki są dużo prostsze od polskich. </p>

<p>Gdy ktoś uczy się polskiego, to musi poznać całą odmianę przez przypadki. Nie wystarczy, że zapamięta samo słowo <q>słońce</q>, musi poznać jeszcze <q>słońca, słońcu, słońcem</q>. Podobnie <q>las, lasu, lasem, lesie</q>.</p>

<p>W języku norweskim wystarczy, że uczysz się rzeczownika z rodzajnikiem i to z tego rodzajnika wynika, jaką musisz dodać końcówkę. Nie ma odmiany przez przypadki.</p>

<p>Zróbmy jeszcze kilka przykładów.</p>

<p><i class="mark">En himmel</i> to <q>jakieś niebo</q>. W formie określonej zniknie rodzajnik i dodamy końcówkę <i class="mark">-en</i>. Dlatego będzie <i class="mark">himmelen</i>.</p>

<dl class="przewodnik-eg">
  <dt>en himmel – himmelen</dt>
  <dd>niebo</dd>
</dl>

<p><i class="mark">Ei mark</i> to niezamieszkały obszar poza miastem, często z lasem. Gdy mówimy o konkretnym, zabierzemy rodzajnik i dodamy końcówkę <i class="mark">-a</i> – <i class="mark">marka</i>.</p>

<dl class="przewodnik-eg">
  <dt>ei mark – marka</dt>
  <dd>niezamieszkały obszar poza miastem, często z lasem</dd>
</dl>

<p>Spotkasz się z tym słowem często, gdziekolwiek nie zamieszkasz w Norwegii, na przykład. <i class="mark">Oslomarka</i>, <i class="mark">Fredrikstadmarka</i>, <i class="mark">Nesoddmarka</i>, <i class="mark">Mossemarka.</i> </p>

<p>Gdy Norweg powie, że idzie na <i class="mark">marka</i> – <i class="mark">Jeg går tur i marka.</i> to znaczy, że jedzie pod miasto do lasu lub nad jezioro.</p>

<p>A jezioro po norwesku to <i class="mark">et vann</i> – mamy rodzajnik więc forma nieokreślona. W formie określonej musimy zabrać rodzajnik i dodać końcówkę <i class="mark">-et</i>. Dlatego mówimy <i class="mark">vannet</i>. Pamiętasz, że nie czytamy ostatniego <i class="mark">-t</i>? Dobrze. :)</p>

<dl class="przewodnik-eg">
  <dt>et vann – vannet</dt>
  <dd>jezioro, woda</dd>
</dl>

<p>Możesz się jeszcze zastanawiać, kiedy używam której formy?</p>

<p>Na początku nauki nie jest to problemem. Nie przejmuj się jeśli czasem nie będziesz wiedzieć, której formy użyć w zdaniu. Najpierw naucz się je tworzyć i rozróżniać, a z czasem poznasz więcej zasad używania ich w zdaniach.</p>

<p>Oczywiście musimy porządnie przećwiczyć tworzenie formy określonej, prawda?</p>

<p>W wyzwaniu czeka na Ciebie wiele nowych przydatnych słów.</p>