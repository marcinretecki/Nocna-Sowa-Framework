<p>W języku polskim każdy rzeczownik ma rodzaj: męski, żeński albo nijaki. Podobnie jest po norwesku, jednak działa to trochę inaczej.</p>



<p>Każdego nowego rzeczownika będziemy uczyli się razem z rodzajnikiem. Są tylko trzy: <i class="mark">en</i>, <i class="mark">ei</i>, <i class="mark">et</i>. Rodzajniki wskazują, jakiego rodzaju jest rzeczownik.</p>



<p>Po polsku nie mamy rodzajników, więc wiele osób, która zaczyna naukę norweskiego ma taki pomysł, żeby je pominąć. Nie ma ich po polsku, to po co się nimi przejmować?</p>

<p>No bo czym właściwie jest ten rodzajnik: <i class="mark">en</i>, <i class="mark">ei</i>, <i class="mark">et</i>? Czy on coś znaczy? Np. w słowie <i class="mark">en vei</i>. Jeśli powiem samo <i class="mark">vei</i> to przecież każdy zrozumie, że to droga. Wydaje się, że szybciej nauczymy się słów bez rodzajnika. </p>



<p>Musisz jednak wiedzieć, że jeśli nie znasz rodzajnika, to nie znasz do końca słowo. I już na samym początku drogi zgubisz się w lesie. A później zniechęcisz do norweskiego. </p>

<p>Zobacz, skąd wiesz, jakiego rodzaju jest słowo po polsku? Skąd wiesz, że mówimy <q>to słońce</q>, a nie <q>ta słońce</q>?</p>

<p>Od dziecka Twoi rodzice, nauczyciele i babcia poprawiali Cię na każdym kroku. Robili to tak długo, aż rodzaje rzeczowników stały się dla Ciebie w pełni naturalne.</p>

<p>Stąd wiesz, że mówimy ten las, ale ta łąka. Ten kwiat, ale ta ścieżka. To drzewo, ale ten krzew.</p>

<p>Podobnie uczą się norweskie dzieci.</p>

<p>Problem w tym, że Ty pewnie nie masz norweskich rodziców i kilkunastu lat na naukę norweskiego, prawda?</p>

<p>Dlatego zaufaj nam, że szybszą drogą nauki norweskiego nie jest pominięcie rodzajników, ale wzięcie się za nie od samego początku. </p>

<p>Naszą aplikację zaprojektowaliśmy inaczej niż jakikolwiek podręcznik i cały pierwszy dział poświęcimy rzeczownikowi.</p>



<p>OK. Już wiesz, że mamy trzy rodzaje. Nie będziemy ich nazywali jak po polsku, męski, żeński i nijaki, żeby nie mieszało się to z norweskim.</p>

<p>Rodzaje w języku polskim nie zgadzają się z norweskim. Na przykład po norwesku <q>słońce</q> jest rodzaju żeńskiego: <i class="mark">ei sol</i>, natomiast po polsku nijakiego: <q>to słońce</q>, a nie <q>ta słońce</q>.</p>

<p>Jeśli nie chcesz, żeby Twoja nauka poprawnego norweskiego trwała kilkanaście lat, musisz każdy nowy rzeczownik, którego chcesz się nauczyć, zapisywać i powtarzać razem z rodzajnikiem. Niektóre słowa mogą mieć kilka rodzajów. Na początku nie ma znaczenia, który wybierzesz.</p>

<p>I tak <q>wycieczka</q> to <i class="mark">en tur</i>. Nigdy nie uczysz się samego <i class="mark">tur</i>, tylko zawsze razem z rodzajnikiem.</p>

<p><q>Domek letniskowy</q> albo <q>chatka</q> to <i class="mark">ei hytte</i>. Jeśli będziesz robić notatki, to musisz zanotować to razem: <i class="mark">ei hytte</i>.</p>

<p><q>Góra</q> to <i class="mark">et fjell</i>. Jeśli nie zapiszesz rodzajnika, to może Ci się wydawać, że jest rodzaju żeńskiego – przecież mówimy po polsku <q>ta góra</q>. Ale po norwesku jest <i class="mark">et</i>.</p>

<p>Dobrze. Możesz się jeszcze zastanawiać, czy to słychać, kiedy pominiesz rodzajnik w zdaniu, albo zamienisz go na inny.</p>

<p>Oczywiście i to od razu.</p>

<p>Jeśli powiesz <i class="mark">et fyr</i> to znaczy latarnia morska, a jeśli zmienisz rodzajnik na <i class="mark">en</i> będzie znaczyło facet. </p>

<dl class="przewodnik-eg">
<dt>et fyr</dt>
<dd>latarnia</dd>
<dt>en fyr</dt>
<dd>facet</dd>
</dl>

<p>Czasem zmiana rodzajnika może zmienić sens wypowiedzi i sprawić, że rozmówca Cię nie zrozumie.</p>

<p>Ale to nie wszystko.</p>

<p>Bo wyobraź sobie obcokrajowca, który uczy się polskiego. Wybieracie się razem na spacer do lasu i on nagle mówi:
<q>Jaka ładna drzewo!</q></p>

<p>Co pomyślisz? No spoko, przecież się uczy. Rozumiem. Ale błąd zauważysz natychmiast.</p>

<p>A po chwili powie: <q>Wielkie niedźwiedź nadchodzi!</q></p>

<p>Może chciał powiedzieć: wielki niedźwiedź. A może wielka niedźwiedzica. Cokolwiek. Ludzie są z reguły życzliwi. Poprawią albo nie. Ale zawsze słyszą. I założę się, że Ty nie chcesz tak mówić.</p>

<p>A żeby tworzyć takie frazy po norwesku, też będzie potrzebna znajomość rodzajnika. Od niego zależy odmiana rzeczownika, odmiana przymiotnika – żeby nie mówić <q>wielka las</q> – i zaimków dzierżawczych (mój, moja, moje itd.).</p>



<p>Jeśli teraz pominiesz naukę rodzajników, to w przyszłości te zagadnienia sprawią Ci niepotrzebne problemy. Wtedy się obudzisz i pomyślisz, że może lepiej jednak poznać rodzajniki. Ale wtedy trzeba wracać do podstawowych słów, uczyć się ich na nowo. Na to nie ma nikt ochoty.</p>



<p>Dlatego nie ryzykuj i każdego słowa ucz się razem z rodzajnikiem. Dzięki temu zaoszczędzisz sobie wielu kłopotów w dalszej nauce norweskiego.</p>

