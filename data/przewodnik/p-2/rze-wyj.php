<p>Wyjątki to nudna rzecz. Nikt ich nie lubi. Norwegowie używają ich bez zastanowienia, a my musimy się pilnować zanim czegoś głupio nie powiemy. Więc może lepiej odmieniać wszystko na siłę regularnie? Albo pozwolić, żeby samo przyszło z czasem?</p>

<p>Może kiedyś przyjdzie. Jak już będziesz używać norweskiego na co dzień, to wiele rzeczy po prostu przyjdzie. Tylko my chcemy pomóc Ci w komunikacji z innymi tu i teraz, a bez kilkudziesięciu podstawowych słów będziesz przez cały czas niepewnie używać języka</p>

<p>Pokażemy Ci zatem kilka reguł, które będą się powtarzały w odmianie rzeczowników. Po nauczeniu się pierwszych, kolejne będziesz odmieniać intuicyjnie, zgodnie z tym samym schematem.</p>

<p>Oczywiście początki są trudne i zniechęcające. Niektórzy robią wszystko, żeby tych słów nie zapamiętać na samą myśl o tym, że to nieregularna odmiana. </p>

<p>Ale z Tobą będzie inaczej. <i class="mark">Ta det med ro</i>. (Weź to na spokojnie). Wiemy co robimy. :)</p>

<p>Zacznijmy od rzeczowników, które kończą się na <i class="mark">-er, -el</i> w podstawowej formie. One często odmieniają się trochę inaczej. Na przykład:</p>

<dl class="przewodnik-eg">
  <dt>en jungel</dt>
  <dd>dżungla</dd>
</dl>

<p>Formę określoną pojedynczą robimy zawsze regularnie. Tu nie ma problemu.</p>

<dl class="przewodnik-eg">
  <dt>jungelen</dt>
  <dd>(ta) dżungla</dd>
</dl>

<p>W liczbie mnogiej skracamy <i class="mark">e</i> z końcówki <i class="mark">-el</i>. Dlatego jest:</p>

<dl class="przewodnik-eg">
  <dt>jungler – junglene</dt>
  <dd>dżungle</dd>
</dl>

<p>Tak samo będzie z innymi rzeczownikami zakończonymi en <i class="mark">-el</i>.</p>

<dl class="przewodnik-eg">
  <dt>en mandel – mandelen – mandler – mandlene</dt>
  <dd>migdał, migdałek</dd>
  <dt>en nudel – nudelen – nudler – nudlene</dt>
  <dd>makaron, kluska</dd>
  <dt>en handel – handelen – handler – handlene</dt>
  <dd>handel</dd>
</dl>

<p>Gdy poznasz więcej takich słów, stanie się to w pełni naturalne – kwestia czasu.</p>

<p>Część z tych rzeczowników ma też podwójną spółgłoskę. Na przykład:</p>

<dl class="przewodnik-eg">
  <dt>en vaffel – vaffelen</dt>
  <dd>gofr</dd>
</dl>

<p>Widzisz podwójne <i class="mark">f</i>? W liczbie mnogie zniknie jedno z nich razem z <i class="mark">e</i>:</p>

<dl class="przewodnik-eg">
  <dt>vafler – vaflene</dt>
  <dd>gofry</dd>
</dl>

<p>Tak samo odmienimy <i class="mark">en tommel</i>, które ma podwójne <i class="mark">m</i>.</p>

<dl class="przewodnik-eg">
  <dt>en tommel – tommelen – tomler – tomlene</dt>
  <dd>kciuk</dd>
</dl>

<p><i class="mark">En tittel</i>, które ma podwójne <i class="mark">t</i>.</p>

<dl class="przewodnik-eg">
  <dt>en tittel – tittelen – titler – titlene</dt>
  <dd>tytuł</dd>
</dl>

<p>I <i class="mark">en nøkkel</i>, które ma podwójne <i class="mark">k</i>.</p>

<dl class="przewodnik-eg">
  <dt>en nøkkel – nøkkelen – nøkler – nøklene</dt>
  <dd>klucz</dd>
</dl>

<p>OK, na razie omówiliśmy końcówki <i class="mark">-el</i>, zostały jeszcze końcówki <i class="mark">-er</i>. Z tą końcówką są zazwyczaj nazwy zawodów. Na przykład:</p>

<dl class="przewodnik-eg">
  <dt>en fisker</dt>
  <dd>rybak</dd>
</dl>

<p>W formie określonej pojedynczej nic się nie dzieje, dodajemy zwyczajnie końcówkę <i class="mark">-en</i>.</p>

<dl class="przewodnik-eg">
  <dt>fiskeren</dt>
  <dd>(ten) rybak</dd>
</dl>

<p>W liczbie mnogiej zamiast kolejnego <i class="mark">-er</i> dodamy samo <i class="mark">-e</i></p>

<dl class="przewodnik-eg">
  <dt>fiskere</dt>
  <dd>rybacy</dd>
</dl>

<p>A w formie określonej mnogiej, zamiast <i class="mark">-ene</i> dodajemy <i class="mark">-ne</i></p>

<dl class="przewodnik-eg">
  <dt>fiskerne</dt>
  <dd>(ci) rybacy</dd>
</dl>

<p>Czytamy też trochę inaczej, bo przez <i class="mark">æ</i>. Tak samo będzie z </p>

<dl class="przewodnik-eg">
  <dt>en jeger – jegeren – jegere – jegerne</dt>
  <dd>myśliwy</dd>
  <dt>en vegetarianer – vegetarianeren – vegetarianere – vegetarianerne</dt>
  <dd>wegetarianin</dd>
  <dt>en jogger – joggeren – joggere – joggerne</dt>
  <dd>biegacz</dd>
</dl>

<p>Wiem, że te zasady nie brzmią najłatwiej przy pierwszym kontakcie, ale skoncentruj się jeszcze przez chwilę, bo to już ostatnia prosta z rzeczownikiem.</p>

<p>W niektórych słowach występuje zmiana samogłoski, czyli przegłos. Jest też wiele rzeczowników, które mają nieregularną odmianę i tych musisz nauczyć się na pamięć. </p>

<p>Sporo z nich przećwiczysz w wyzwaniu, ale przez całą naukę możesz spotkać się z nowymi wyjątkami. Tak już jest z norweskim.</p>

<p>Ze słów, które zawsze się przydadzą są nazwy członków rodziny. Po norwesku, prawie cała rodzina jest nieregularna. Na przykład:</p>

<dl class="przewodnik-eg">
  <dt>en bror</dt>
  <dd>brat</dd>
</dl>

<p>Określona forma w liczbie pojedynczej zawsze jest regularna, więc będzie</p>

<dl class="przewodnik-eg">
  <dt>broren</dt>
  <dd>(ten) brat</dd>
</dl>

<p>Ale w liczbie mnogiej:</p>

<dl class="przewodnik-eg">
  <dt>brødre – brødrene</dt>
  <dd>bracia</dd>
</dl>

<p>Podobnie jest z:</p>

<dl class="przewodnik-eg">
  <dt>ei søster – søstera – søstre – søstrene</dt>
  <dd>siostra</dd>
  <dt>ei mor – mora – mødre – mødrene</dt>
  <dd>matka</dd>
  <dt>ei datter – dattera – døtre – døtrene</dt>
  <dd>córka</dd>
</dl>

<p>I tak dalej. Jeśli masz dużą rodzinę i lubisz o niej opowiadać, możesz rozrysować drzewo genealogiczne i rozpisać wszystkie nazwy po norwesku razem z odmianą. Przykład znajdziesz w treści pod filmem. (link)</p>

<p>Jeśli coś trenujesz, to pewnie przydadzą Ci się nazwy części ciała. One też odmieniają się nieregularnie.</p>

<dl class="przewodnik-eg">
  <dt>ei hånd – hånda – hender – hendene</dt>
  <dd>ręka</dd>
  <dt>et kne – kneet – knær – knærne</dt>
  <dd>kolano</dd>
  <dt>en skulder – skulderen – skuldre – skuldrene</dt>
  <dd>bark</dd>
  <dt>en fot – foten – føtter – føttene</dt>
  <dd>stopa</dd>
</dl>

<p>I tu znów możesz wydrukować rysunek ciała, na którym opiszesz najpotrzebniejsze słowa razem z odmianą.</p>

<p>Wiem, co teraz myślisz. <q>Tak tego dużo, że nauka zajmie lata!</q> To prawda. Jest wiele wyjątków. W dodatku niektóre słowa mają kilka wariantów odmiany.</p>

<p>Damy Ci jedną wskazówkę. Jeśli spotkasz się z rzeczownikiem, który odmienia się na kilka sposobów, ucz się tylko jednego wariantu – tego który wydaje Ci się najbardziej logiczny lub intuicyjny. Lepiej umieć dobrze jedną odmianę, niż znać wszystkie po trochu i bać się odezwać.</p>

<p>Wiemy, że nie jesteś w stanie opanować na raz wszystkich wyjątków, ale możesz nauczyć się tych najczęściej spotykanych. Dzięki temu całkiem sprawnie poradzisz sobie w codziennych sytuacjach. Przecież o to chodzi, żeby jak najszybciej zacząć mówić, prawda?</p>

<p>Na start przygotowaliśmy wyzwanie, a w nim najbardziej przydatne wyjątki. Możesz zrobić je wiele razy, bo wiemy, że niektóre słowa mylą się nawet zaawansowanym.</p>