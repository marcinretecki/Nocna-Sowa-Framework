<p>Wpiszmy jakiś rzeczownik, może <i class="mark">skog</i>?</p>

<p>Naciśnijmy <i class="mark">Bokmål</i>, bo to jest język pisany, którego się uczysz.</p>

<p>Zaraz obok wyszukanego słowa, <i class="mark">skog</i>, jest <i class="mark">m1</i>. Cyfrą nie przejmuj się, ważniejsza jest litera <i class="mark">m</i>. Ona oznacza, że <i class="mark">skog</i> jest rodzaju <i class="mark">en</i> (<i class="mark">maskulinum</i>). Po polsku <q>męski</q> też zaczyna się na <i class="mark">m-</i> więc łatwo to skojarzysz.</p>

<p><i class="mark">Skog</i> ma też dwie pisownie i obie są tu podane.</p>

<p>Naciśnijmy na <i class="mark">m1</i>. </p>

<p>Pojawia się wtedy tabela, która pokazuje całą odmianę tego słowa. Omówimy ją po kolei w następnych przewodnikach, teraz nas interesuje rodzajnik <i class="mark">en</i>. Jeśli chcesz zanotować to słowo, to zapiszesz <i class="mark">en skog</i>.</p>

<p>Dobrze, poszukajmy innego słowa. <i class="mark">Sol</i>.</p>

<p><i class="mark">Sol</i> ma obok dwa symbole <i class="mark">m</i> oraz <i class="mark">f</i>. <i class="mark">f</i> oznacza <i class="mark">femininum</i>, czyli rodzaj <i class="mark">ei</i>. </p>

<p>Dlaczego są dwa symbole? W języku norweskim, wszystkie rzeczowniki rodzaju <i class="mark">ei</i> można też traktować jak rodzaju <i class="mark">en</i>. (Wszystkie słowa rodzaju ei są jednocześnie en.)</p>

<p>To znaczy, że część Norwegów nie używa nigdy <i class="mark">ei</i>. Zamiast <i class="mark">ei sol</i>, mówią <i class="mark">en sol</i>. Druga grupa Norwegów zawsze, gdzie to poprawne używa <i class="mark">ei</i>. Trzecia grupa, najbardziej liczna jest gdzieś pomiędzy. Część słów rodzaju <i class="mark">ei</i> użyją z rodzajnikiem <i class="mark">en</i>, a część z <i class="mark">ei</i>.</p>

<p>Czy to znaczy, że można dowolnie sobie wybrać? Tak, ale nie na samym początku nauki.</p>

<p>My chcielibyśmy nauczyć Cię rozumienia norweskiego w jak najszerszym zakresie. A to wiąże się również z nauką rzeczowników rodzaju <i class="mark">ei</i>.</p>

<p>Naciśnijmy na <i class="mark">m1, f1</i>.</p>

<p>Mamy tu tabelę z obiema formami.
<i class="mark">En sol</i> oraz <i class="mark">ei sol</i>.</p>

<p>Na początek wybierz <i class="mark">ei</i>, a jeśli z czasem uznasz, że Twoi znajomi tak nie mówią, albo forma żeńska nie jest dla Ciebie naturalna, z łatwością się przestawisz. </p>

<p>Zawsze łatwiej jest pomyśleć, OK, wszystkie rodzaju <i class="mark">ei</i> są przecież <i class="mark">en</i>. Niż na nowo zapamiętywać wszystkie słowa, które mogą być tego rodzaju, jeśli w przyszłości zdecydujesz się go używać.</p>

<p>Wyszukajmy jeszcze jedno słowo.</p>

<p><i class="mark">Tre</i>. obok jest <i class="mark">n</i>, czyli <i class="mark">nøytrum</i> (nijaki). To jest rodzaj <i class="mark">et</i>.</p>

<p>Ale zobacz, <i class="mark">tre</i> ma całkiem spory wpis w tym słowniku, bo ma więcej znaczeń. Gdy będziesz szukać samodzielnie słów, musisz się na to wyczulić.</p>

<p>Poniżej jest <i class="mark">tre</i> z oznaczeniem <i class="mark">det</i>. To znaczy, że to jest liczba, a nie rzeczownik.</p>

<p>Jeszcze niżej jest <i class="mark">verb</i> - czasownik (czasem oznaczany samym <i class="mark">v</i>). To też nie jest rodzaj rzeczownika.</p>

<p>Jak szukasz rzeczowników, to interesują Cię tylko skróty <i class="mark">m</i> <i class="mark">f</i> <i class="mark">n</i>. Mogą być z liczbami, które oznaczają sposób odmiany.</p>

<p>Naciśnijmy na <i class="mark">n</i> przy <i class="mark">tre</i>.</p>

<p>Jest <i class="mark">et tre</i>, więc wszystko się zgadza.</p>

<p>Są jeszcze słowa, które są stworzone z kilku mniejszych słów.</p>

<p>Zacznijmy od <q>sprzęt</q>. Po norwesku <q>sprzęt kuchenny</q> to <i class="mark">kjøkkenutstyr</i>, a <q>sprzęt komputerowy</q> – <i class="mark">datautstyr</i>.</p>

<p>Jeśli wyszukamy te słowa w słowniku, nie będą tam podane jako osobne hasła. Ale jak widzisz oba te słowa mają taką samą podstawę – <i class="mark">utstyr</i>, co znaczy <q>sprzęt</q>. Wyszukajmy go.</p>

<p>Tu widzisz, że mamy oznaczenie <i class="mark">n1</i>, czyli rodzaj <i class="mark">et</i>. Taki sam rodzaj będą więc miały słowa <i class="mark">kjøkkenutstyr</i> oraz <i class="mark">datautstyr</i>. </p>

<p>To znaczy, że patrzymy na ostatni człon złożonego rzeczownika i to zgodnie z nim będzie rodzaj całego słowa.</p>

<p>W wyzwaniu czeka na Ciebie kilka słów do samodzielnego sprawdzenia w słowniku. W Bokmålsordboka możesz znaleźć rodzajnik, a w Lexin tłumaczenie słowa.</p>


