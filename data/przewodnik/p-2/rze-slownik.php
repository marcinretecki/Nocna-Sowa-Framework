<p>Wiesz już, że nauka rodzajników jest ważna. </p>

<p>W Lesie wszystkie słowa będą dla Ciebie przetłumaczone i napisane razem z rodzajnikami, ale chcielibyśmy nauczyć Cię też samodzielnej nauki.</p>

<p>Na szczęście nie musisz niczego kupować, bo dwa najlepsze słowniki są dostępne za darmo w internecie. Jeden jest tworzony przez Uniwersytet w Bergen i Språkrådet (Radę języka norweskiego), a drugi przez Utdanningsdirektoratet przy norweskim Ministerstwie Edukacji.</p>

<p><a href="http://ordbok.uib.no/" target="_blank">Bokmålsordboka</a> oraz Nynorskordboka są uważane za standard poprawności języka norweskiego. Gdy Norweg nie wie, czy słowo albo odmiana jest poprawna, sprawdza ją tu. Z tych słowników można też korzystać na egzaminach w norweskich szkołach podstawowych i średnich.</p>

<p>Jest też dostępna wersja drukowana, ale najbardziej aktualna wersja i tak jest w internecie lub aplikacji.</p>

<p>Drugim słownikiem jest <a href="http://lexin.udir.no/?mode=main-page&sub-mode=search&dict=nbo-pol-maxi&ui-lang=nbo" target="_blank">Lexin</a>, w którym znajdziesz tłumaczenia na język polski, angielski oraz wiele przykładowych zastosowań szukanego słowa. </p>

<p>Z czasem będziesz używać tych słowników coraz częściej, ale na początek zobacz, jak możesz sprawdzić rodzaj rzeczownika.</p>

