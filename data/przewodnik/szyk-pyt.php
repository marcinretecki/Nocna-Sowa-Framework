<p>Pytania w języku norweskim mają sztywny szyk. To znaczy, że nie możesz w nich przestawić żadnego elementu.</p>

<p>Nie wystarczy też zmiana intonacji, choćby dlatego, że Norwegowie intonują zdania inaczej niż Polacy i mogą źle Cię zrozumieć. Więc to kolejność słów decyduje o tym, czy jest to pytanie czy twierdzenie.</p>

<p>Czy pamiętasz pytania przez <i class="mark">czy</i>?</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Husker du det?</dt>
  <dd>(Czy) Pamiętasz?</dd>
</dl>

<p>W norweskim nie mamy <i class="mark">czy</i>, tylko przestawiamy kolejność słów. W każdym pytaniu będzie najpierw orzeczenie, a następnie podmiot. </p>

<p>Do tego możemy dołożyć zaimki pytające. Wtedy szyk pytania wygląda tak:</p>

<p class="con">zaimek pytający + orzeczenie + podmiot + <i>ikke</i> + co? jak? gdzie? kiedy? dlaczego?</p>

<p>Zauważ, że kolejność pytań <i class="mark">co?</i> <i class="mark">jak?</i> <i class="mark">gdzie?</i> <i class="mark">kiedy?</i> <i class="mark">dlaczego?</i> jest taka sama, jak w zdaniach twierdzących. Oczywiście żadna z tych informacji nie jest obowiązkowa, bo do stworzenia poprawnego pytania wystarczy orzeczenie i podmiot.</p>

<p>Zróbmy razem kilka przykładów:</p>


<pre>
  - tu chyba jest błąd w opisie tego pytania
  - det jest podmiotem
</pre>

<dl class="dl-eg przewodnik-eg">
  <dt>Hva er det?</dt>
  <dd>Co to jest?</dd>
</dl>

<p><i class="mark">Hva</i> to zaimek pytający</p>

<p><i class="mark">er</i> to orzeczenie</p>

<p><i class="mark">det</i> odpowiada na pytanie <i class="mark">co?</i></p>

<dl class="dl-eg przewodnik-eg">
  <dt>Hvem går på sopptur?</dt>
  <dd>Kto idzie na grzybobranie?</dd>
</dl>

<p>W tym pytaniu nie ma podmiotu, bo o niego pytamy.</p>

<p><i class="mark">Hvem</i> to zaimek pytający</p>

<p><i class="mark">går</i> to czasownik w czasie teraźniejszym, więc orzecznie</p>

<p><i class="mark">på sopptur</i> odpowiada na pytanie <i class="mark">gdzie?</i></p>

<dl class="dl-eg przewodnik-eg">
  <dt>Hva driver du med?</dt>
  <dd>Czym się zajmujesz?</dd>
</dl>

<p><i class="mark">Hva</i> to oczywiście zaimek pytający.</p>

<p><i class="mark">driver</i> to orzeczenie</p>

<p><i class="mark">du</i> to podmiot</p>

<p>A co zrobić z <i class="mark">med</i>? Dlaczego zostało na końcu pytania?</p>

<p>Musimy wrócić do zdania twierdzącego, żeby to miało sens.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Jeg driver med forskning.</dt>
  <dd>Zajmuję się badaniami.</dd>
</dl>

<p>Ja zajmuję się <i class="mark">czym?</i> badaniami. Po polsku musimy <q>badania</q> odmienić, żyby zdanie było poprawne. Po norwesku nie ma przypadków, więc dodajemy odpowiednie przyimki.</p>

<p>Gdy pytamy o to, czym się zajmujesz, to nadal użyjemy całej frazy <i class="mark">å drive med</i>. Czasownik odmieniamy i stawiamy na swojej pozycji, a <i class="mark">med</i> będzie w miejscu <i class="mark">co?</i>.</p>

<p>Z pytaniem <i class="mark">co?</i> musimy być elastyczni, bo tu kryją się też polskie przypadki <i class="mark">czego?</i> <i class="mark">czym?</i> <i class="mark">z czym?</i> <i class="mark">o czym?</i>.</p>

<p>Podobna sytuacja jest w pytaniu</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Hva snakker dere om nå?</dt>
  <dd>O czym rozmawiacie teraz?</dd>
</dl>

<p><i class="mark">Om</i> działa razem z <i class="mark">hva</i>. Po polsku stawiamy to obok siebie tworząc <q>o czym</q>, ale po norwesku, <i class="mark">om</i> zostaje na swoim miejscu.</p>

<p>Łatwo to zauważyć w zdaniu twierdzącym i pytaniu przez <i class="mark">czy</i>.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Dere snakker om jobben nå.</dt>
  <dd>Wy rozmawiacie o pracy teraz.</dd>
  <dt>Snakker dere om jobben nå?</dt>
  <dd>Czy rozmawiacie o pracy teraz?</dd>
  <dt>Hva snakker dere om nå?</dt>
  <dd>O czym rozmawiacie teraz?</dd>
</dl>

<p>Zamieniamy orzeczenie z podmiotem, żeby stworzyć pytanie i dodajemy zaimek.</p>

<p>Praca znika, bo to jest informacja, o którą pytamy.</p>

<p>Widzisz? </p>

<p>Dobrze, zbudujmy razem dłuższe pytanie.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Hvem</dt>
  <dd>kto</dd>
</dl>

<p>To będzie zaimek pytający.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>vil</dt>
  <dd>chce</dd>
</dl>

<p>czasownik w czasie teraźniejszym, więc orzeczenie.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>gå</dt>
  <dd>iść</dd>
</dl>

<p>bezokolicznik – wstawimy w miejsce <i class="mark">co?</i></p>

<dl class="dl-eg przewodnik-eg">
  <dt>på fjellet</dt>
  <dd>na górę, w góry</dd>
</dl>

<p>pasuje w miejscu <i class="mark">gdzie?</i></p>

<dl class="dl-eg przewodnik-eg">
  <dt>i morgen</dt>
  <dd>jutro</dd>
</dl>

<p>to oczywiście <i class="mark">kiedy?</i></p>

<dl class="dl-eg przewodnik-eg">
  <dt>for å se utsikten over byen</dt>
  <dd>żeby zobaczyć widok na miasto</dd>
</dl>

<p><i class="mark">Po co?</i> <i class="mark">dlaczego?</i> – żeby zobaczyć widok</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Hvem vil gå på fjellet i morgen for å se utsikten over byen?</dt>
  <dd>Kto chce pójść na górę, żeby zobaczyć widok na miasto?</dd>
</dl>

<p>Norweski i polski rządzą się innymi prawami. Pytania, które po polsku dobrze brzmią, przetłumaczone słowo w słowo nie mają po norwesku sensu.</p>

<p>Jak zapytamy: <q>Ile lat ma to drzewo?</q></p>

<p>Po norwesku nie ma słowa <i class="mark">ile</i>. W dodatku nie można posiadać wieku.</p>

<p>Takie pytanie ułożymy używając <i class="mark">gammel</i>. Zapytamy dosłownie <q>Jak stare jest to drzewo?</q></p>

<p>Po kolei</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Hvor gammelt</dt>
  <dd>jak stare</dd>
</dl>

<p>Ta fraza będzie w miejscu zaimka pytającego. Dodaliśmy <i class="mark">-t</i>, bo drzewo jest rodzaju <i class="mark">et</i>.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>er</dt>
  <dd>jest</dd>
</dl>

<p>to orzeczenie</p>

<dl class="dl-eg przewodnik-eg">
  <dt>treet</dt>
  <dd>drzewo</dd>
</dl>

<p>to podmiot</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Hvor gammelt er treet?</dt>
  <dd>Ile lat ma to drzewo?</dd>
</dl>

<p>Wielu rzeczy nie da się przetłumaczyć dosłownie. Dlatego tak ważne jest, żeby dużo czytać, słuchać i powtarzać po norwesku.</p>

<p>Masz ochotę przećwiczyć więcej pytań? Zrób wyzwanie tyle razy, aż poczujesz, że jest to łatwe i oczywiste. Pamiętaj, trening czyni mistrza.</p>