<p>Czy pamiętasz co to jest podmiot?</p>

<p>To część zdania, która w stronie czynnej jest wykonawcą czynności.</p>

<p>Zgadza się?</p>

<p>Jeśli nie masz pewności, to zobacz na przykładzie.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Håkon lager vafler.</dt>
  <dd>Håkon przygotowuje gofry.</dd>
</dl>

<p><i class="mark">Håkon</i> jest podmiotem i wykonawcą czynności, przygotowywania. Po norwesku nie ma odmiany przez przypadki, dlatego tak ważny jest szyk zdania. Dzięki temu wiemy, że to nie gofry przygotowują Håkona, ale Håkon gorfy.</p>

<p>Czy moglibyśmy pominąć podmiot? I powiedzieć samo: <i class="mark">lager vafler</i>?</p>

<p>Wtedy nie byłoby jasne, kto wykonuje czynność. Dlatego podmiot jest potrzebny.</p>

<p>Możemy zastąpić też imię zaimkiem:</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Han lager vafler.</dt>
  <dd>On przygotowuje gofry.</dd>
</dl>

<p>I wtedy <i class="mark">han</i> jest podmiotem.</p>

<p>Zdarzy się, że podmiotem będzie więcej niż jedna osoba. Na przykład:</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Barna elsker å lære.</dt>
  <dd>Dzieci uwielbiają się uczyć.</dd>
</dl>

<p>Dzieci są w tym zdaniu podmiotem i jednocześnie wykonawcą czynności.</p>

<p>Podmiotem może być także rzeczownik. Na przykład:</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Parken ligger ved elva.</dt>
  <dd>Park leży nad rzeką.</dd>
</dl>

<p>Park jest podmiotem i wykonawcą czynności.</p>

<p>Podmiotem nie musi być tylko jedno słowo. Możemy na przykład powiedzieć:</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Plantene i denne skogen er veldig sjeldne.</dt>
  <dd>Rośliny w tym lesie są bardzo rzadkie.</dd>
</dl>

<p>Tu na miejscu podmiotu jest cała fraza <i class="mark">plantene i denne skogen</i>.</p>

<p>Tylko w zdaniach rozkazujących nie będzie podmiotu, ale we wszystkich innych powinien się znaleźć. Nawet jeśli myśląc po polsku, nie zawsze nam pasuje.</p>

<p>Jak powiesz: <q>wieje</q>?</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Det blåser.</dt>
  <dd></dd>
</dl>

<p>Jeśli nie mamy osoby, która wykonuje czynność, to używamy zaimka <i class="mark">det</i>, który jest w tym zdaniu podmiotem.</p>

<p>Tak samo powiesz o padającym śniegu lub deszczu:</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Det snør.</dt>
  <dd>Pada śnieg.</dd>
  <dt>Det regner.</dt>
  <dd>Pada deszcz.</dd>
</dl>

<p>Gdy jest zimno, powiesz:</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Det er kaldt.</dt>
  <dd>Jest zimno.</dd>
</dl>

<p>Podobnie, kiedy mówisz komuś, że jest w porządku, dobrze, użyjesz konstrukcji z <i class="mark">det</i>.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Det går kjempebra!</dt>
  <dd>Jest bardzo dobrze.</dd>
</dl>

<p>Czasem w miejscu podmiotu spotkasz się ze słowem <i class="mark">man</i>, które nie oznacza mężczyzny, bo ten byłby przez <i class="mark">nn</i>.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Man må lære mye for å oppfylle sine drømmer.</dt>
  <dd>Trzeba dużo się uczyć, żeby spełnić swoje marzenia.</dd>
</dl>

<p>Po polsku powiedzielibyśmy: <q>Trzeba dużo się uczyć, żeby spełnić swoje marzenia.</q> I nie mamy w tym zdaniu podmiotu. Żeby powiedzieć to po norwesku pomyśl w ten sposób: <q>Człowiek musi uczyć się dużo, żeby spełnić swoje marzenia.</q></p>

<p>Jak widzisz podmiot jest obowiązkowy, żeby wyrazić taką myśl.</p>

<p>Podobnie będzie w wielu przysłowiach czy powiedzeniach, kiedy nie kierujemy zdania bezpośrednio do kogoś, ale mamy na myśli ogół ludzi.</p>

<dl class="dl-eg przewodnik-eg">
  <dt>Man må lære å krype før man kan lære å gå.</dt>
  <dd>Trzeba nauczyć się pełzać, zanim można nauczyć się chodzić.</dd>
</dl>

<p>Zapamiętaj, że każde norweskie zdanie powinno mieć podmiot, który jest ściśle związany z orzeczeniem, czyli czynnością, którą wykonuje.</p>

<p>Żeby sprawdzić, czy dobrze rozumiesz czym jest podmiot zrób szybkie wyzwanie.</p>